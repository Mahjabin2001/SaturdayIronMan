package com.upskill.assignment_2;

public class Q5 {

	public static void main(String[] args) {
		multiplication();
	}

	public static void multiplication() {
		for (int i = 8; ;) {
			for (int j = 1; j <= 10; j++) {
				System.out.println(i * j + " ");
			}
			System.out.println();
			break;
		}

	}
}
